/* ERROR CODE FORMAT (refer to Advanced Windows Book pg 759)
    (also see RaiseException() documentation)

   Bits 31-30 0 = Success    (most significant)
              1 = Information
              2 = Warning
              3 = Error
   Bit  29    0 = Microsoft
              1 = Customer
   Bits 28    MUST BE ZERO
   Bits 27-16 Facility Code (programmer defined)
   Bits 15-0  Exception Code (programmer defined)   (least sigificant)

*/

/*
      NOTES:
       0xE0000000 is bits 29,30,31 on (Error, Customer)
*/

